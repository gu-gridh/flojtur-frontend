          <p>
            Pehr (Petrus/Petter/Per) Strand föddes i Arnäs församling, strax
            utanför Örnsköldsvik i Ångermanland.<sup>2</sup> Hans födelse och
            dop finns inte noterade i kyrkoböckerna. När han senare återfinns i
            församlingens husförhörslängder tillsammans med sin familj, så är
            hans födelsedatum inte möjligt att otvetydigt fastställa. I
            litteraturen förekommer ofta 1756 som födelseår, ibland preciserat
            till den 15 januari, men någon källa för detta har inte anförts. I
            Strands bouppteckning anges dock att ”Orgelbyggeri Directeuren Pehr
            Strand” var 68 år gammal när han avled den 19 augusti 1826.<sup
              >3</sup
            >
            Ännu mera precis är angivelsen i notisen den 8 september i Post- och
            Inrikes Tidningar<sup>4</sup> där det konstateras att Strand var 68
            ½ år gammal då han avled. Dessa uppgifter pekar entydigt på att
            Strand föddes 1758, troligen i januari.
          </p>
          <p>
            Pehr Strands far var båtsman, vilket innebar att han periodvis
            tjänstgjorde i den svenska örlogsflottan. Med detta följde rätten
            till ett båtsmanstorp som tillhandahölls av ett antal gårdar i
            hemförsamlingen. Pehr växte alltså upp under ganska enkla
            förhållanden i båtsmanstorpet, och flyttade som ung till Stockholm
            för att bli bildhuggare. Från 1777 gick han i lära hos en av de
            främsta bildhuggarna i Stockholm, Johan Ljung (1717–1787). Johan
            Ljung arbetade bland annat med utsmyckningen av Kungliga slottet i
            Stockholm.<sup>5</sup> I oktober 1784 gifte sig Pehr Strand med
            Rebecca Nordström.<sup>6</sup> Pehr och Rebecca Strand fick sju barn
            men endast tre levde till vuxen ålder: Jonas Samuel (1786–1860),
            verksam som organist och orgelbyggare i Vingåker; Pehr Zacharias
            (1797–1844), som kom att driva utvecklingen av svenskt orgelbyggeri
            framåt; och Magdalena Henriette (1800–1884).
          </p>
          <p>
            Pehr Strand utbildade sig alltså till bildhuggare, men fick snart
            flera strängar på sin lyra. Han är idag kanske främst känd som
            orgel- och spelursbyggare. Väl känt är att han 1791 ansökte om
            privilegium att få tillverka spelur med valsar.<sup>7</sup> I
            ansökan intygas att han har nödvändiga kunskaper för detta, men
            tyvärr saknas originalhandlingar utvisande var han lärt sig detta
            hantverk. En rimlig förklaring är att han lärde sig det finmekaniska
            hantverket hos någon etablerad urmakare i Stockholm. Något som tyder
            på det är att det i privilegiebeslutet konstateras att Strand lovar
            att inte själv tillverka urverken till speluren utan vända sig till
            någon etablerad mästare. Det kan ha varit en medveten taktik för att
            förekomma kritik från urmakarmästare om att han inkräktade på deras
            område. Vissa av speluren är dock på urtavlan signerade Pehr eller
            Petter Strand. Eventuellt kan detta betyda att de i sin helhet är
            tillverkade senare, när Strand etablerat sig och när urmakarämbetet
            försvagats, vilket skedde mot slutet av 1700-talet.<sup>8</sup> Ett
            belägg för att Strand kunde bygga urverk är en signerad väggpendyl,
            en gåva till dottern Henriette, som fortfarande finns i släktens
            ägo. Att signera urtavlan på en liten väggpendyl han själv inte
            byggt vore förvånande.
          </p>
          <figure class="span-columns">
            <img src="http://placekitten.com/1500/1350" />
            <figcaption>
              Väggur signerat ”Pehr Strand Stockholm”. Har sannolikt tillhört
              Henriette Löfman, född Strand. Foto: Johan Norrback.
            </figcaption>
          </figure>
          <p>
            Mindre känt har det varit att Strand även ansökt om och beviljats
            ytterligare ett privilegium. Det gällde korkskäreri, och den
            verksamheten kom att bedrivas parallellt med hans
            lacktillverkning.<sup>9</sup> Lacktillverkningen verkar inte ha
            krävt tillstånd, varför någon privilegieansökan från Strand inte
            finns för den verksamheten. Om det är fråga om lack för sigill eller
            för målning klargörs inte, men möjligen gäller det sigillack. Sådan
            tillverkning finns beskriven, medan lack för målning troligen
            tillverkades av hantverkaren själv efter behov.<sup>10</sup> Hur
            korkskäreriet samverkar med övriga verksamheter är oklart men
            tillsammans med sigillacktillverkning verkar de ha utgjort
            sidoinkomster då dessa hantverk inte behövs för spelurstillverkning.
            Utöver spelurstillverkningen är Strand i Sverige bäst känd som
            orgelbyggare. Den ljudalstrande delen i ett spelur med pipor, ett
            flöjtur, är i praktiken en liten orgel, så hantverket är i stor
            utsträckning detsamma. Redan 1789 anges han som byggare av orgeln i
            Tysslinge kyrka strax väster om Örebro.<sup>11</sup> Var han lärde
            sig orgelbyggarhantverket är inte heller känt, men stilistiskt
            ansluter han till det som kallas Stockholmsskolan inom det svenska
            orgelbyggeriet, med företrädare som Jonas Gren (1715–65), Petter
            Stråhle (1720–65) och Olof Schwan (1744–1812). Den äldsta bevarade
            orgeln av Pehr Strand finns i Sundal-Ryrs gamla kyrka i Dalsland.
            Det är en liten kammarorgel med tre stämmor, byggd 1795 och signerad
            inne i väderlådan, på vilken alla pipor står uppställda. Allt som
            allt byggde Strand ett trettiotal orglar. Det var förmodligen
            särskilt hedersamt för honom att han fick uppdraget att bygga en ny
            orgel åt hemförsamlingen Arnäs strax utanför Örnsköldsvik. Orgeln
            stod målad och klar 1808.<sup>12</sup>
          </p>
          <p>
            Ur källorna framträder en bild av lärlingen, sedermera direktören,
            Pehr Strand som en driftig person med många olika kompetenser. Mot
            slutet av hans karriär växte också verkstaden, och enligt
            dokumentationen i Fabriksberättelserna på Stockholms stadsarkiv hade
            han flest anställda år 1819. Då var han registrerad både under
            Urfabriker och Musikaliska instrumentmakare, och antal arbetare var
            7 respektive 8, totalt 15 personer.<sup>13</sup> Strands första
            utbildning hos bildhuggaren Ljung gav honom också tillgång till ett
            kontaktnät där hans kommande kunder bland kungligheter, adel och
            bruksägare återfanns.
          </p>
