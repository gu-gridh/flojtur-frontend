          </blockquote>
          <p>
            Denna korta beskrivning från slutet av 1700-talet är hämtad från
            Pehr Thams (1737–1820) reseskildring ”ifrån Westergöthland til
            Stockholm, gjord åren 1796 och 1797”. Tham, med ett stort intresse
            för historia och arkeologi, följde tidens praxis med
            reseskildringar. Han var hovintendent och rörde sig i kretsarna där
            Strands spelur senare kom att bli populära. Thams karaktärisering av
            Strands stil som varande enligt den ”Berlinske Methoden” är en
            viktig samtida iakttagelse som även bekräftas av hantverket i
            Strands spelur. Detta återkommer vi till.
          </p>
        </div>
        <div class="articletext">
          <figure class="span-columns">
