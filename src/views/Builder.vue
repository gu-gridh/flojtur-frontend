        <p>
          Mindre känt har det varit att Strand även ansökt om och beviljats
          ytterligare ett privilegium. Det gällde korkskäreri, och den
          verksamheten kom att bedrivas parallellt med hans
          lacktillverkning.<sup>9</sup> Lacktillverkningen verkar inte ha krävt
          tillstånd, varför någon privilegieansökan från Strand inte finns för
          den verksamheten. Om det är fråga om lack för sigill eller för målning
          klargörs inte, men möjligen gäller det sigillack. Sådan tillverkning
          finns beskriven, medan lack för målning troligen tillverkades av
          hantverkaren själv efter behov.<sup>10</sup> Hur korkskäreriet
          samverkar med övriga verksamheter är oklart men tillsammans med
          sigillacktillverkning verkar de ha utgjort sidoinkomster då dessa
          hantverk inte behövs för spelurstillverkning. Utöver
          spelurstillverkningen är Strand i Sverige bäst känd som orgelbyggare.
          Den ljudalstrande delen i ett spelur med pipor, ett flöjtur, är i
          praktiken en liten orgel, så hantverket är i stor utsträckning
          detsamma. Redan 1789 anges han som byggare av orgeln i Tysslinge kyrka
          strax väster om Örebro.<sup>11</sup> Var han lärde sig
          orgelbyggarhantverket är inte heller känt, men stilistiskt ansluter
          han till det som kallas Stockholmsskolan inom det svenska
          orgelbyggeriet, med företrädare som Jonas Gren (1715–65), Petter
          Stråhle (1720–65) och Olof Schwan (1744–1812). Den äldsta bevarade
          orgeln av Pehr Strand finns i Sundal-Ryrs gamla kyrka i Dalsland. Det
          är en liten kammarorgel med tre stämmor, byggd 1795 och signerad inne
          i väderlådan, på vilken alla pipor står uppställda. Allt som allt
          byggde Strand ett trettiotal orglar. Det var förmodligen särskilt
          hedersamt för honom att han fick uppdraget att bygga en ny orgel åt
          hemförsamlingen Arnäs strax utanför Örnsköldsvik. Orgeln stod målad
          och klar 1808.<sup>12</sup>
        </p>

