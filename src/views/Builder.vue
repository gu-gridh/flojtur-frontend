        <p>
          Pehr Strands far var båtsman, vilket innebar att han periodvis
          tjänstgjorde i den svenska örlogsflottan. Med detta följde rätten till
          ett båtsmanstorp som tillhandahölls av ett antal gårdar i
          hemförsamlingen. Pehr växte alltså upp under ganska enkla förhållanden
          i båtsmanstorpet, och flyttade som ung till Stockholm för att bli
          bildhuggare. Från 1777 gick han i lära hos en av de främsta
          bildhuggarna i Stockholm, Johan Ljung (1717–1787). Johan Ljung
          arbetade bland annat med utsmyckningen av Kungliga slottet i
          Stockholm.<sup>5</sup> I oktober 1784 gifte sig Pehr Strand med
          Rebecca Nordström.<sup>6</sup> Pehr och Rebecca Strand fick sju barn
          men endast tre levde till vuxen ålder: Jonas Samuel (1786–1860),
          verksam som organist och orgelbyggare i Vingåker; Pehr Zacharias
          (1797–1844), som kom att driva utvecklingen av svenskt orgelbyggeri
          framåt; och Magdalena Henriette (1800–1884).
        </p>
        <p>
          Pehr Strand utbildade sig alltså till bildhuggare, men fick snart
          flera strängar på sin lyra. Han är idag kanske främst känd som orgel-
          och spelursbyggare. Väl känt är att han 1791 ansökte om privilegium
          att få tillverka spelur med valsar.<sup>7</sup> I ansökan intygas att
          han har nödvändiga kunskaper för detta, men tyvärr saknas
          originalhandlingar utvisande var han lärt sig detta hantverk. En
          rimlig förklaring är att han lärde sig det finmekaniska hantverket hos
          någon etablerad urmakare i Stockholm. Något som tyder på det är att
          det i privilegiebeslutet konstateras att Strand lovar att inte själv
          tillverka urverken till speluren utan vända sig till någon etablerad
          mästare. Det kan ha varit en medveten taktik för att förekomma kritik
          från urmakarmästare om att han inkräktade på deras område. Vissa av
          speluren är dock på urtavlan signerade Pehr eller Petter Strand.
          Eventuellt kan detta betyda att de i sin helhet är tillverkade senare,
          när Strand etablerat sig och när urmakarämbetet försvagats, vilket
          skedde mot slutet av 1700-talet.<sup>8</sup> Ett belägg för att Strand
          kunde bygga urverk är en signerad väggpendyl, en gåva till dottern
          Henriette, som fortfarande finns i släktens ägo. Att signera urtavlan
          på en liten väggpendyl han själv inte byggt vore förvånande.
        </p>
