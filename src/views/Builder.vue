        <p>
          Ur källorna framträder en bild av lärlingen, sedermera direktören,
          Pehr Strand som en driftig person med många olika kompetenser. Mot
          slutet av hans karriär växte också verkstaden, och enligt
          dokumentationen i Fabriksberättelserna på Stockholms stadsarkiv hade
          han flest anställda år 1819. Då var han registrerad både under
          Urfabriker och Musikaliska instrumentmakare, och antal arbetare var 7
          respektive 8, totalt 15 personer.<sup>13</sup> Strands första
          utbildning hos bildhuggaren Ljung gav honom också tillgång till ett
          kontaktnät där hans kommande kunder bland kungligheter, adel och
          bruksägare återfanns.
        </p>
