      <h1>Om Pehr Strand</h1>
      <div class="articleIngress">
        <p>
          <em>Orgel- och spelursbyggaren Pehr Strand</em> (publicerad i
          <em>1700-tal: Nordic Journal for Eighteenth-Century Studies</em>
